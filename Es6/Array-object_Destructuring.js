var arr = ["Hello", "World"]  
  
// destructuring assignment  
var [first, second] = arr;  
  
console.log(first); // Hello  
console.log(second); // World

// object destructuring

const num = {x: 100, y: 200};  
const {x, y} = num;  
  
console.log(x); // 100  
console.log(y); // 200  